using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MethodPractice
{
    public class MathMethods
    {
        public int MyAdd(int x)
        {
            int sum = x + 2;
            return sum;
        }

        public int MySub(int x)
        {
            int diff = x - 2;
            return diff;
        }

        public int MyMult(int x)
        {
            int prod = x * 2;
            return prod;
        }
    }
}
